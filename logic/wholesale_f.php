Users must be preapproved to visit this part of the site.

1. They signin w/ username and pwd.
2. Query wholesale customer table to check if valid.
	a. If not valid, show errors.	
3. Users get to grid order form with standard wholesale prices.
	a. If less than 50 yards of latex product in stock, gray out row.
4. Users add items to cart and perform checkout.  
	a. Checkout will not ask for payment info.
		1. They will state check or wire.
5. On Checkout
	a. Order and user info saved to dbase.
	b. Invoice emailed to user.
	c. User prompted to print completion screen.

*** Orders outside the US and Canada will not be given shipping rates.  Rates will be emailed after a 
custom quote is obtained. ***